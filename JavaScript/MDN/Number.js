/*
The Number() function
*/
// An example of how it works:
const dan = '10';
const trim = true;

// Converting dan to a number
const number = Number(dan);
console.log(number); // Outputs: 10

// Converting trim to a number
const number2 = Number(trim);
console.log(number2); // Outputs: 1

// Prints the Number() type
console.log(Number); // Prints the Number() type.

// Using booleans to test the Number() function
console.log(Number(dan) === 10); // Prints 'true'
